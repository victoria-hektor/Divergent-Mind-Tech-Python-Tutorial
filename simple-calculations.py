# Simple Calculations
a = 5
b = 2

# Calculations
addition = a + b 
subtraction = a - b
multiplication = a * b
division = a / b
percentage_of_a = (a/b) * 100 # percentage of a relative to b

# print the results
print("--- calculations ---")
print(f"Addition (a+b): {addition}")
print(f"subtraction (a-b): {subtraction}")
print(f"Multiplication (a*b): {multiplication}")
print(f"Division (a/b): {division:.2f}") # limit to 2 decimal places
print(f"Percentage of a relative to b: {percentage_of_a:.2f}") # limit to 2 decimal places